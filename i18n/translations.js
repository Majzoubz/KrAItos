export function tFor(lang, key, vars) {
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;
  let str = dict[key] || TRANSLATIONS.en[key] || key;
  if (vars) {
    for (const k of Object.keys(vars)) {
      str = str.split('{' + k + '}').join(String(vars[k]));
    }
  }
  return str;
}

let _activeLang = 'en';
export function setActiveLanguage(code) {
  if (code && TRANSLATIONS[code]) _activeLang = code;
}
export function getActiveLanguage() { return _activeLang; }
export function tActive(key, vars) { return tFor(_activeLang, key, vars); }
